#'   \item{Ptot}{Précipitation totale moyenne normale 30 ans}
#'   \item{GrwDays}{Nombre de jours de la saison de croissance moyenne normale 30 ans}
#'   \item{Vigueur}{Code de vigueur de l'arbre}
#'   \item{MSCR}{Code MSCR}
#'   \item{ABCD}{Code ABCD}
#'   \item{Sup_PE}{Superficie de la placette en ha}
#'   \item{Annee_Coupe}{Année de la dernière coupe partielle}
#'   \item{ntrt}{Nombre de coupes partielles}
#'   \item{Pente}{Classe de pente de la placette}
#' }
#' @examples
#' Test400m2Temoin
"Test400m2Temoin"


